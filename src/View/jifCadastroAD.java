    public boolean buscarAluno() {
        boolean resp = false;
        DefaultListModel lista = new DefaultListModel();
        String sql;
        sql = "from Aluno a where a.CPF =:cpf";
        SessionFactory sf = HibernateUtil.getSessionFactory();
        Session session = sf.openSession();
        Query query = session.createQuery(sql);
        query.setString("cpf", jTfCPF.getText());
        List<Aluno> results = (List<Aluno>) query.list();
        if (results.size() > 0) {
            resp = true;
            this.aluno = results.get(0);
        } else {
            JOptionPane.showMessageDialog(null, "Aluno não encontrado!");
        }

        session.close();

        return resp;
    }

    public boolean buscarDisciplina() {
        boolean resp = false;
        String sql;
        sql = "from Disciplina d where d.nome =:nome";
        SessionFactory sf = HibernateUtil.getSessionFactory();
        Session session = sf.openSession();
        Query query = session.createQuery(sql);
        query.setString("nome", jTfDisciplinaNome.getText());
        List<Disciplina> results = (List<Disciplina>) query.list();
        if (results.size() > 0) {
            resp = true;
            this.codDisciplina = results.get(0);
        } else {
            JOptionPane.showMessageDialog(null, "Disciplina não encontrada!");
        }

        session.close();

        return resp;
    }

