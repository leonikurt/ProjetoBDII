    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

        if (buscarAluno() && buscarDisciplina()) {
            SessionFactory af = HibernateUtil.getSessionFactory();
            Session session = af.openSession();
            Transaction tx = session.beginTransaction();
            try{

                    Query query = session.createSQLQuery("CALL InsertTurma(:cpf,:disciplina,:nome)");
                    query.setString("cpf", this.aluno.getCPF());
                    query.setInteger("disciplina", this.codDisciplina.getCodigo());
                    query.setString("nome", jTfTurmaNome.getText());
                    int result = query.executeUpdate();
                    
                    if (result > 0) {
                        JOptionPane.showMessageDialog(null, "Aluno cadastrado na disciplina!");
                    } else {
                        throw new Exception("Houve um problema no cadastro.");
                    }
                
                tx.commit();
            }
            catch(GenericJDBCException e){                 
                JOptionPane.showMessageDialog(null,e.getCause().getMessage());
//                af.getCurrentSession().getTransaction().rollback();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null,ex.getMessage());
//                af.getCurrentSession().getTransaction().rollback();
            }
            finally{
                session.flush();
                session.close();
            }
        }

    }//GEN-LAST:event_btnCadastrarActionPerformed


