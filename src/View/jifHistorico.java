    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (check() && buscarAluno() && buscarCurso()) {
            SessionFactory af = HibernateUtil.getSessionFactory();
            Session session = af.openSession();
            Transaction tx = session.beginTransaction();
            try{
                    String sql = "call InsereHistorico('"+jTfCurso.getText()+"','"+jTfMatricula.getText()+"',"+Float.parseFloat(jTfNota.getText())+",'2018/12/04')";
                    Query query = session.createSQLQuery("call InsereHistorico(:curso,:matricula,:nota,:data)");
                    query.setString("matricula", jTfMatricula.getText());
                    query.setString("curso", jTfCurso.getText());
                    query.setFloat("nota", Float.parseFloat(jTfNota.getText()));
                    query.setString("data", "2018/12/04");
                    int result = query.executeUpdate();
                    
                    JOptionPane.showMessageDialog(null, "Historico registrado.");
                
                    tx.commit();
            }
            catch(GenericJDBCException e){                 
                JOptionPane.showMessageDialog(null,e.getCause().getMessage());
//                af.getCurrentSession().getTransaction().rollback();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null,ex.getMessage());
//                af.getCurrentSession().getTransaction().rollback();
            }
            finally{
                session.flush();
                session.close();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

