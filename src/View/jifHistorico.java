    public boolean check(){
        boolean resp = false;
        
        if(jTfMatricula.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Digite a matricula!");
        }
        else if(jTfCurso.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Digite o !");
        }
        else if(jTfNota.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Digite a nota!");
        }
        else{
            resp = true;
        }
        
        return resp;
    }
    
    public boolean buscarAluno() {
        boolean resp = false;
        DefaultListModel lista = new DefaultListModel();
        String sql;
        sql = "Select a.* from Aluno a "
                + " inner join Matricula m On m.cpf_aluno = a.cpf "
                + " where m.matricula_aluno = :matricula";
        SessionFactory sf = HibernateUtil.getSessionFactory();
        Session session = sf.openSession();
        
        Query query = session.createSQLQuery(sql);
        query.setString("matricula", jTfMatricula.getText());
        List<Aluno> results = (List<Aluno>) query.list();
        if (results.size() > 0) {
            this.aluno = results.get(0);
            resp = true;
        } else {
            JOptionPane.showMessageDialog(null, "Aluno não encontrado!");
        }

        session.close();

        return resp;
    }
    
    public boolean buscarCurso(){
        boolean resp = false;
        
        String sql;
        sql = "Select c.* from Curso c where c.nome = :nome ";
        SessionFactory sf = HibernateUtil.getSessionFactory();
        Session session = sf.openSession();
        
        Query query = session.createSQLQuery(sql);
        query.setString("nome", jTfCurso.getText());
        List<Curso> results = (List<Curso>) query.list();
        if (results.size() > 0) {
            resp = true;
        } else {
            JOptionPane.showMessageDialog(null, "Curso não encontrado!");
        }

        session.close();
        
        return resp;
    }

